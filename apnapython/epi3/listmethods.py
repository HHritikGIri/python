list =[7,2,8,3,9,11]

#append:Adds an element at the end of the list
list.append(29)
print(list)

#Sort:Sorts list in ascending order
list.sort()
print(list)

#Reverse-Sort: sorts list in descending order
list.sort(reverse=True)
print(list)

#Reverse:reverses the original list
list.reverse()
print(list)

#insert:insert element at index
list.insert(2,39)
print(list)

#Remove:removes first occurence of element
list.remove(39)
print(list)

#Pop:Removes element at index
list.pop(4)
print(list)  #removes 9 from list

